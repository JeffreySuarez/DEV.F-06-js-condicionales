let dia = prompt("Ingresa un día de la semana");

if (dia === "Lunes") {
  alert("Hoy es Lunes");
} else if (dia === "Viernes") {
  alert("Hoy es Viernes");
} else if (dia === "Sabado") {
  alert("Hoy es Sabado");
} else if (dia === "Domingo") {
  alert("Hoy es Domingo");
} else {
  alert("Tu respuesta no es válida");
}
